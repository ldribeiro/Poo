package inscricao2;

public class Curso2 {

	private int id;
	private String nome;
	
	public Curso2(int id, String nome) {
		super();
		this.id = id;
		this.nome = nome;
	}
	
	public Curso2() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
}
